#include <bits/stdc++.h>
using namespace std;

// binary AND operator

int main() {
     int a , b;

     cin>>a>>b;

     // and

    cout << (a & b) << endl; // 2

    // or

    cout << (a | b) << endl; // 7

    // xor
    cout << (a ^ b) << endl; // 5

    // not
    cout << (~a) << endl; // -6
}